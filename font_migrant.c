static void rotate_counter_clock90_16x8(unsigned char *in, unsigned char *out)
{
    int i,j;
    unsigned char tmp1, tmp2;
    for(i = 0; i < 8; i++){
	tmp1 = 0;
	for(j = 0; j < 8; j++){
	    tmp2 = in[j] & (1 << i);
	    tmp2 >>= i;
	    tmp1 <<= 1;
	    tmp1 |= tmp2;
	}
	out[i*2] = tmp1;
	    
	tmp1 = 0;
	for(j = 0; j < 8; j++){
	    tmp2 = in[j] & (1 << i);
	    tmp2 >>= i;
	    tmp1 <<= 1;
	    tmp1 |= tmp2;
	}
	out[i*2 + 1] = tmp1;
	
    }
}

static void rotate_counter_clock90(unsigned char *in, unsigned char *out)
{
    int i,j;
    unsigned char tmp1,tmp2;
    for(i = 0; i < 8; i++){

	tmp1 = 0;
	for(j = 0; j < 8; j++){
	    tmp2 = in[j] & (1 << i);
	    tmp2 >>= i;
	    tmp1 <<= 1;
	    tmp1 |= tmp2;
	}
	out[i] = tmp1;
    }
}

static void print_buf(unsigned char *buf, int size)
{
    int i;
    for(i = 0; i < size; i++){
	if( i != 0 && (i % 8) == 0){
	    printf("\n");
	}
	printf("0x%02X, ", buf[i]);
    }
    printf("\n");
}

